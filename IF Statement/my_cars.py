# Using if statement to satisfy more than one condition
# list of cars that I have driven
my_cars = ['toyota', 'nissan', 'volkwagen']

# check the different cars that I have driven
if 'toyota' in my_cars:
    print(f"Toyota was your first car!")
if 'Audi' in my_cars:
    print("You have not driven Audi!")
if 'nissan' in my_cars:
    print(f"Nissan was your second car!")
if 'volkwagen' in my_cars:
    print(f"Volks Wagen was your third car!")

# Stating the cars that I have driven
print("\nAbove list are the cars that you have driven!")