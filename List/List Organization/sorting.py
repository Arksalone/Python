hustle = ['uber', 'lyft', 'doordash', 'grubhub']
hustle.sort() # print my hustle in alphabetical order permanently

print(hustle)
# The original list can never be redone
print(hustle)

# sort the list in reversed alphabetical order permanently
hustle.sort(reverse=True)
print(hustle)

# The original list can never be redone
print(hustle)